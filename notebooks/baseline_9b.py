PERSUASION_STRATEGIES = {
    'logical-appeal', 'emotion-appeal', 'credibility-appeal',
    'foot-in-the-door', 'self-modeling', 'personal-story',
    'donation-information', 'proposition-of-donation',
    'ask-donate-more', 'personal-related-inquiry',
    'task-related-inquiry', 'source-related-inquiry',
    'confirm-donation', 'ask-donation-amount'
}

POSITIVE_STRATEGIES = {
    'personal-story', 'donation-information', 'confirm-donation',
    'positive-to-inquiry', 'self-modeling', 'logical-appeal',
    'emotion-appeal', 'praise-user'
}

NEGATIVE_STRATEGIES = {
    'ask-donate-more', 'neutral-to-inquiry', 'ask-not-donate-reason'
}

POSITIVE_REACTIONS = {
    'agree-donation', 'positive-reaction-to-donation',
    'provide-donation-amount', 'confirm-donation',
    'ask-donation-procedure', 'ask-org-info'
}

NEGATIVE_REACTIONS = {
    'disagree-donation', 'negative-reaction-to-donation',
    'disagree-donation-more'
}

def build_dialogue_summary(dialogue_id, df_dialog, persuadee_info):
    dlg = df_dialog[df_dialog['dialogue_id'] == dialogue_id].copy()
    info_row = persuadee_info[persuadee_info['dialogue_id'] == dialogue_id]

    if info_row.empty:
        return None

    info = info_row.iloc[0]
    donated_flag  = int(info['donated'])
    actual_amount = float(info['actual_donation'])
    num_turns     = int(info['num_turns']) if not pd.isna(info['num_turns']) else 0

    # Strategy counts
    persuader_rows = dlg[dlg['role'] == 0]
    strategies_used = {}
    for col in ['persuader_label_1', 'persuader_label_2']:
        for label in persuader_rows[col].dropna():
            if label in PERSUASION_STRATEGIES:
                strategies_used[label] = strategies_used.get(label, 0) + 1

    positive_used = {k: v for k, v in strategies_used.items() if k in POSITIVE_STRATEGIES}
    negative_used = {k: v for k, v in strategies_used.items() if k in NEGATIVE_STRATEGIES}

    # Persuadee reactions
    persuadee_rows = dlg[dlg['role'] == 1]
    reactions = {}
    for label in persuadee_rows['persuadee_label_1'].dropna():
        reactions[label] = reactions.get(label, 0) + 1

    positive_reactions = {k: v for k, v in reactions.items() if k in POSITIVE_REACTIONS}
    negative_reactions = {k: v for k, v in reactions.items() if k in NEGATIVE_REACTIONS}

    # Sentiment
    avg_sentiment_pos   = dlg['sentiment_pos'].mean()
    persuader_sentiment = persuader_rows['sentiment_pos'].mean()
    persuadee_sentiment = persuadee_rows['sentiment_pos'].mean()

    # Resistance → agreement pattern
    persuadee_labels_seq = persuadee_rows['persuadee_label_1'].dropna().tolist()
    had_initial_resistance = any(r in NEGATIVE_REACTIONS for r in persuadee_labels_seq[:3])
    eventual_agreement     = 'agree-donation' in persuadee_labels_seq

    # Format conversation
    conversation_lines = []
    for _, row in dlg.iterrows():
        speaker = "Persuader" if row['role'] == 0 else "Persuadee"
        conversation_lines.append(f"{speaker}: {row['utterance']}")
    conversation_text = "\n".join(conversation_lines)

    return {
        'dialogue_id'          : dialogue_id,
        'donated'              : donated_flag,
        'actual_amount'        : actual_amount,
        'num_turns'            : num_turns,
        'conversation_text'    : conversation_text,
        'strategies_used'      : strategies_used,
        'positive_strategies'  : positive_used,
        'negative_strategies'  : negative_used,
        'reactions'            : reactions,
        'positive_reactions'   : positive_reactions,
        'negative_reactions'   : negative_reactions,
        'avg_sentiment_pos'    : round(avg_sentiment_pos, 3),
        'persuader_sentiment'  : round(persuader_sentiment, 3),
        'persuadee_sentiment'  : round(persuadee_sentiment, 3),
        'had_initial_resistance': had_initial_resistance,
        'eventual_agreement'   : eventual_agreement,
    }


