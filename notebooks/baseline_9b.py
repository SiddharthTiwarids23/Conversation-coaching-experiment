def generate_coaching_feedback(s):
    outcome_str = "successful (the persuadee donated)" if s['donated'] else "unsuccessful (the persuadee did not donate)"
    amount_str  = f"${s['actual_amount']:.2f}" if s['donated'] else "nothing"

    # Section 1: Overall
    if s['donated']:
        if s['had_initial_resistance'] and s['eventual_agreement']:
            overall = (
                f"This was a successful persuasion conversation — the persuadee donated {amount_str}. "
                f"Notably, the persuadee showed initial resistance but was eventually convinced, "
                f"which reflects effective adaptive persuasion by the persuader."
            )
        else:
            overall = (
                f"This was a successful persuasion conversation — the persuadee donated {amount_str}. "
                f"The persuader maintained a positive tone throughout and guided the persuadee "
                f"toward a donation decision effectively."
            )
    else:
        overall = (
            f"This persuasion conversation was unsuccessful — the persuadee did not donate. "
            f"While the persuader made genuine efforts, the approach had gaps that "
            f"prevented the persuadee from committing to a donation."
        )

    # Section 2: Strengths
    strengths = []
    if 'personal-story' in s['positive_strategies']:
        strengths.append("Used personal storytelling effectively — sharing personal narratives builds emotional connection and increases donation likelihood.")
    if 'logical-appeal' in s['positive_strategies']:
        strengths.append("Incorporated logical arguments and factual information about the cause, which helps persuadees who prefer evidence-based reasoning.")
    if 'emotion-appeal' in s['positive_strategies']:
        strengths.append("Made emotional appeals to highlight the impact on children, which is one of the most effective strategies in charity persuasion.")
    if 'credibility-appeal' in s['strategies_used']:
        strengths.append("Established credibility by providing factual information about Save the Children's mission and track record.")
    if 'donation-information' in s['positive_strategies']:
        strengths.append("Clearly explained the donation procedure, which reduces friction and increases the persuadee's confidence in taking action.")
    if 'self-modeling' in s['positive_strategies']:
        strengths.append("Used self-modeling by expressing personal donation intent, which creates social proof and encourages the persuadee to follow.")
    if s['positive_reactions']:
        reaction_list = ', '.join(s['positive_reactions'].keys())
        strengths.append(f"Elicited positive persuadee responses including: {reaction_list}, indicating the persuader's approach resonated at key moments.")
    if s['persuader_sentiment'] > 0.3:
        strengths.append(f"Maintained a positive and warm conversational tone (average positivity score: {s['persuader_sentiment']:.2f}), which creates a comfortable atmosphere for donation decisions.")
    if not strengths:
        strengths.append("The persuader engaged with the persuadee and attempted to initiate a conversation about the cause.")

    # Section 3: Weaknesses
    weaknesses = []
    if 'ask-donate-more' in s['negative_strategies']:
        weaknesses.append("Pushed for a larger donation amount after initial agreement — this can feel greedy and risks reversing the persuadee's commitment. It is better to accept the first agreed amount gratefully.")
    if not s['donated'] and 'personal-story' not in s['strategies_used']:
        weaknesses.append("Did not use personal storytelling — conversations without personal narratives show significantly lower donation rates. Sharing a story creates emotional investment in the cause.")
    if not s['donated'] and 'emotion-appeal' not in s['strategies_used']:
        weaknesses.append("Lacked emotional appeal — purely informational approaches are less effective. Describing the real human impact on individual children is more persuasive than statistics alone.")
    if s['negative_reactions']:
        reaction_list = ', '.join(s['negative_reactions'].keys())
        weaknesses.append(f"The persuadee showed clear resistance signals ({reaction_list}) that were not adequately addressed. When resistance appears, switching strategy — for example from logical to emotional appeal — is more effective than repeating the same approach.")
    if 'neutral-to-inquiry' in s['negative_strategies']:
        weaknesses.append("Gave neutral or non-committal responses to the persuadee's questions, which signals uncertainty and weakens credibility. Every response should reinforce confidence in the cause.")
    if not weaknesses:
        if s['donated']:
            weaknesses.append("No major weaknesses identified. The conversation achieved its goal while maintaining a respectful and engaging tone.")
        else:
            weaknesses.append("The persuader did not adapt their strategy when the persuadee showed disengagement. Flexibility in approach is critical when initial strategies are not working.")

    # Section 4: Suggestions
    suggestions = []
    if not s['donated']:
        if 'personal-story' not in s['strategies_used']:
            suggestions.append("1. Open with a brief personal story or specific example of a child helped by Save the Children before making any donation request. This creates emotional buy-in before asking for commitment.")
        if 'foot-in-the-door' not in s['strategies_used']:
            suggestions.append("2. Use the foot-in-the-door technique — start by asking for a very small amount (e.g. $0.10) before gradually working up. Small initial commitments are psychologically easier to agree to.")
        if 'self-modeling' not in s['strategies_used']:
            suggestions.append("3. Mention your own intention to donate. Social modeling is highly effective — people are more likely to donate when they see others doing so.")
        suggestions.append("4. When the persuadee resists, do not repeat the same strategy. Identify which aspect they object to and address that specific concern with a different approach (logical → emotional → personal).")
        suggestions.append("5. End the conversation with a concrete, low-barrier ask: 'Even $0.25 makes a real difference for a child today.' Specific small amounts are easier to commit to than open-ended requests.")
    else:
        suggestions.append("1. This conversation can serve as a model for future persuasion attempts. The key elements that worked were: " + (', '.join(s['positive_strategies'].keys()) if s['positive_strategies'] else 'consistent engagement and positive tone') + ".")
        suggestions.append("2. Document which specific turn led to the persuadee's agreement and replicate that approach in future conversations.")
        if 'ask-donate-more' in s['negative_strategies']:
            suggestions.append("3. Avoid pushing for more after the persuadee agrees — accept the commitment gracefully and close the conversation positively.")

    feedback = f"""OVERALL PERFORMANCE:
{overall}

WHAT WORKED:
{chr(10).join(f'- {st}' for st in strengths)}

AREAS FOR IMPROVEMENT:
{chr(10).join(f'- {w}' for w in weaknesses)}

SPECIFIC SUGGESTIONS:
{chr(10).join(suggestions)}"""

    return feedback

