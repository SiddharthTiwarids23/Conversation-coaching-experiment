def upload_to_github(filepath, github_path=None, message=None):
    """
    Upload a local file to GitHub repo.
    - filepath: local file path
    - github_path: path in repo (defaults to filename)
    - message: commit message
    """
    if github_path is None:
        github_path = f"artifacts/{os.path.basename(filepath)}"
    if message is None:
        message = f"Upload {os.path.basename(filepath)} from Notebook A"

    with open(filepath, "rb") as f:
        content = base64.b64encode(f.read()).decode("utf-8")

    url = f"{API_URL}/{github_path}"
    headers = {
        "Authorization": f"token {GITHUB_TOKEN}",
        "Accept": "application/vnd.github.v3+json"
    }


    r = requests.get(url, headers=headers)
    sha = None
    if r.status_code == 200:
        sha = r.json()["sha"]

    payload = {
        "message": message,
        "content": content,
        "branch": BRANCH,
    }
    if sha:
        payload["sha"] = sha

    r = requests.put(url, headers=headers, json=payload)
    if r.status_code in (200, 201):
        print(f"   {github_path} — uploaded")
        return True
    else:
        print(f"   {github_path} — HTTP {r.status_code}: {r.json().get('message','')}")
        return False


def download_from_github(github_path, local_path=None):
    """Download a file from the repo's raw content."""
    if local_path is None:
        local_path = os.path.basename(github_path)
    url = f"https://raw.githubusercontent.com/{GITHUB_USER}/{GITHUB_REPO}/{BRANCH}/{github_path}"
    r = requests.get(url)
    if r.status_code == 200:
        with open(local_path, "wb") as f:
            f.write(r.content)
        print(f"   {local_path} ({len(r.content)/1024:.1f} KB)")
        return True
    else:
        print(f"   {github_path} — HTTP {r.status_code}")
        return False

